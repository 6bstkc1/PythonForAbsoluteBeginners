import random

f_num = random.randrange(4) + 1

if f_num == 0:
    print("You will meet the person of your dreams.")
elif f_num == 1:
    print("Your future is looking bright.")
elif f_num == 2:
    print("You will find a new hobby soon.")
elif f_num == 3:
    print("Bring an umbrella because it will rain.")
else:
    print("You will embrace an old friend.")
